def set_font(run, font_name='宋体', size=10.5, bold=False, color=BLACK):
    """设置字体格式"""
    run.font.name = font_name
    run.font.size = Pt(size)
    run.font.bold = bold
    run.font.color.rgb = color
    run._element.rPr.rFonts.set(qn('w:eastAsia'), font_name)

def add_heading(doc, text, level=1):
    """添加标题"""
    if level == 1:
        p = doc.add_heading(text, level=1)
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        for run in p.runs:
            set_font(run, '宋体', 16, bold=True, color=NUAA_BLUE)
    elif level == 2:
        p = doc.add_heading(text, level=2)
        for run in p.runs:
            set_font(run, '宋体', 14, bold=True, color=NUAA_BLUE)
    elif level == 3:
        p = doc.add_heading(text, level=3)
        for run in p.runs:
            set_font(run, '宋体', 12, bold=True, color=NUAA_DARK)
    return p

def add_paragraph(doc, text, indent=False, spacing=(0, 0)):
    """添加段落"""
    p = doc.add_paragraph(text)
    p.paragraph_format.line_spacing = Pt(18)
    if indent:
        p.paragraph_format.first_line_indent = Cm(0.74)
    for run in p.runs:
        set_font(run, '宋体', 10.5)
    return p

def add_table(doc, headers, rows, col_widths=None):
    """添加表格"""
    table = doc.add_table(rows=1, cols=len(headers))
    table.style = 'Table Grid'
    
    # 设置表头
    header_row = table.rows[0]
    for i, header in enumerate(headers):
        cell = header_row.cells[i]
        cell.text = header
        for paragraph in cell.paragraphs:
            for run in paragraph.runs:
                set_font(run, '宋体', 9, bold=True, color=NUAA_BLUE)
        cell.paragraphs[0].alignment = WD_ALIGN_PARAGRAPH.CENTER
    
    # 设置表头背景色（深蓝）
    for cell in header_row.cells:
        tc = cell._tc
        tcPr = tc.get_or_add_tcPr()
        shd = OxmlElement('w:shd')
        shd.set(qn('w:fill'), '003366')
        tcPr.append(shd)
    
    # 添加数据行
    for row_data in rows:
        row = table.add_row()
        for i, cell_data in enumerate(row_data):
            cell = row.cells[i]
            cell.text = str(cell_data)
            for paragraph in cell.paragraphs:
                for run in paragraph.runs:
                    set_font(run, '宋体', 9)
            cell.paragraphs[0].alignment = WD_ALIGN_PARAGRAPH.CENTER
    
    # 设置列宽
    if col_widths:
        for i, width in enumerate(col_widths):
            for row in table.rows:
                row.cells[i].width = Cm(width)
    
    return table

